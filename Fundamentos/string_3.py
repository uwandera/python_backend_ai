nome = "Thomas Uwande Alves Cardoso"
#podemos olhar strings como vetores [start:stop:step]
print(nome[0])#indice é usado para 
print(nome[-2])# numeros negativos são usados para caminha do final para o começõ
print(nome[:9])#começa da posição [0] e vai até a [9], neste caso o ultimo caracter nao sera incluido
print(nome[10:])# apartir do start até o final da string
print(nome[10:16])#sub string start e stop, ao passo 1 a 1
print(nome[10:16:2])# sub string star:stop:step
print(nome[:])# copia da string inteira
print(nome[::-1]) #espelhamento de string, exibir de tras pra frente


#lista pode ser tratada como um tipo de vetor
frutas = ["laranja", "maçã", "uva","banana","carambola","manga"]
print(frutas)

#listas podem ser vazias
frutas = []
print(frutas)

#podemos guardar valores em variaveis de forma transformando-os em vetores 
letras = list("python")
print(letras)

#criar vetores sequenciais usando range
numeros = list(range(10))
print(numeros)

#criar vetores de forma mista
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
